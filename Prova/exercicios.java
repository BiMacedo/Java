package Prova;

public class exercicios {
    private String nome;
    private int repeticoes;

    public exercicios(String nome, int repeticoes) {
        this.nome = nome;
        this.repeticoes = repeticoes;
    }

    public String getNome() {
        return nome;
    }

    public int getRepeticoes() {
        return repeticoes;
    }
}


